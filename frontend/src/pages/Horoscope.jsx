// Main Horoscope Component 
const Horoscope = () => {
  const [selectedRashi, setSelectedRashi] = useState(null);
  const [selectedPeriod, setSelectedPeriod] = useState('daily');
  const [horoscopeData, setHoroscopeData] = useState(null);
  const [loading, setLoading] = useState(false);
  const [rashis, setRashis] = useState([]);
  const [error, setError] = useState(null);

  useEffect(() => { loadRashis(); }, []);
  useEffect(() => { if (selectedRashi) loadHoroscope(); }, [selectedRashi, selectedPeriod]);

  const loadRashis = async () => {
    try {
      const response = await getAllRashis();
      if (response.success && response.rashis) {
        setRashis(response.rashis.map(rashi => ({
          ...rashi,
          color: getColorForRashi(rashi.name),
          bgColor: getBgColorForRashi(rashi.name),
          borderColor: getBorderColorForRashi(rashi.name)
        })));
      } else { setRashis(defaultRashis); }
    } catch (error) { setRashis(defaultRashis); }
  };

  const loadHoroscope = async () => {
    if (!selectedRashi) return;
    setLoading(true);
    setHoroscopeData(null);
    setError(null);
    try {
      const data = await getHoroscope(selectedRashi.name, selectedPeriod);
      setHoroscopeData(data);
    } catch (error) {
      setError("Unable to sync with planetary energies. Retrying...");
      // Auto-retry or fallback logic
    } finally { setLoading(false); }
  };

  const handleRashiClick = (rashi) => {
    setSelectedRashi(rashi);
    setSelectedPeriod('daily');
    window.scrollTo({ top: 0, behavior: 'smooth' });
  };

  const getColorForRashi = (n) => {
    const c = { Aries:'text-red-400', Taurus:'text-emerald-400', Gemini:'text-yellow-400', Cancer:'text-slate-400', Leo:'text-orange-400', Virgo:'text-teal-300', Libra:'text-pink-400', Scorpio:'text-rose-400', Sagittarius:'text-indigo-400', Capricorn:'text-amber-700', Aquarius:'text-sky-400', Pisces:'text-cyan-400' };
    return c[n] || 'text-white';
  };
  const getBgColorForRashi = (n) => {
    const b = { Aries:'bg-red-400/5', Taurus:'bg-emerald-400/5', Gemini:'bg-yellow-400/5', Cancer:'bg-slate-400/5', Leo:'bg-orange-400/5', Virgo:'bg-teal-300/5', Libra:'bg-pink-400/5', Scorpio:'bg-rose-400/5', Sagittarius:'bg-indigo-400/5', Capricorn:'bg-amber-700/5', Aquarius:'bg-sky-400/5', Pisces:'bg-cyan-400/5' };
    return b[n] || 'bg-white/5';
  };
  const getBorderColorForRashi = (n) => {
     const bc = { Aries:'border-red-400/20', Taurus:'border-emerald-400/20', Gemini:'border-yellow-400/20', Cancer:'border-slate-400/20', Leo:'border-orange-400/20', Virgo:'border-teal-300/20', Libra:'border-pink-400/20', Scorpio:'border-rose-400/20', Sagittarius:'border-indigo-400/20', Capricorn:'border-amber-700/20', Aquarius:'border-sky-400/20', Pisces:'border-cyan-400/20' };
     return bc[n] || 'border-white/10';
  };

  const defaultRashis = [
    { name: "Aries", displayName: "Mesh", symbol: "♈", sanskrit: "मेष", element: "Fire", lord: "Mars" },
    { name: "Taurus", displayName: "Vrishabh", symbol: "♉", sanskrit: "वृषभ", element: "Earth", lord: "Venus" },
    { name: "Gemini", displayName: "Mithun", symbol: "♊", sanskrit: "मिथुन", element: "Air", lord: "Mercury" },
    { name: "Cancer", displayName: "Kark", symbol: "♋", sanskrit: "कर्क", element: "Water", lord: "Moon" },
    { name: "Leo", displayName: "Simha", symbol: "♌", sanskrit: "सिंह", element: "Fire", lord: "Sun" },
    { name: "Virgo", displayName: "Kanya", symbol: "♍", sanskrit: "कन्या", element: "Earth", lord: "Mercury" },
    { name: "Libra", displayName: "Tula", symbol: "♎", sanskrit: "तुला", element: "Air", lord: "Venus" },
    { name: "Scorpio", displayName: "Vrishchik", symbol: "♏", sanskrit: "वृश्चिक", element: "Water", lord: "Mars" },
    { name: "Sagittarius", displayName: "Dhanu", symbol: "♐", sanskrit: "धनु", element: "Fire", lord: "Jupiter" },
    { name: "Capricorn", displayName: "Makar", symbol: "♑", sanskrit: "मेष", element: "Earth", lord: "Saturn" },
    { name: "Aquarius", displayName: "Kumbh", symbol: "♒", sanskrit: "कुम्भ", element: "Air", lord: "Saturn" },
    { name: "Pisces", displayName: "Meen", symbol: "♓", sanskrit: "मीन", element: "Water", lord: "Jupiter" }
  ];

  return (
    <Layout>
      <div className="max-w-7xl mx-auto w-full py-8">
        {!selectedRashi ? (
          <>
            <header className="text-center mb-24">
              <h1 className="text-5xl md:text-7xl font-extrabold bg-gradient-to-r from-pink-400 via-purple-300 to-indigo-300 bg-clip-text text-transparent mb-4 tracking-wide">The Horoscope</h1>
              <p className="text-purple-200 text-lg">Vedic Astrology • Managed Way</p>
            </header>

            <div className="grid grid-cols-2 md:grid-cols-4 lg:grid-cols-6 gap-6">
              {(rashis.length > 0 ? rashis : defaultRashis).map((r, i) => (
                <motion.div
                  key={r.name}
                  initial={{ opacity: 0, y: 20 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ delay: i * 0.05 }}
                  onClick={() => handleRashiClick(r)}
                  className={`p-10 rounded-xl ${r.bgColor || 'bg-black/40'} border ${r.borderColor || 'border-purple-600/30'} hover:border-purple-500 cursor-pointer transition-all duration-300 shadow-lg group text-center relative overflow-hidden`}
                >
                   <div className="absolute top-0 left-0 w-full h-1 bg-gradient-to-r from-transparent via-purple-500 to-transparent opacity-0 group-hover:opacity-100 transition-opacity"></div>
                   <div className={`text-6xl mb-6 ${r.color} group-hover:scale-110 group-hover:rotate-6 transition-transform duration-500`}>{r.symbol}</div>
                   <h3 className="text-lg font-semibold text-pink-400">{r.displayName || r.name}</h3>
                   <p className="text-xs text-purple-300 font-semibold uppercase tracking-widest mt-2">{r.sanskrit}</p>
                </motion.div>
              ))}
            </div>
          </>
        ) : (
          <HoroscopeDisplay 
            rashi={selectedRashi} 
            period={selectedPeriod} 
            data={horoscopeData} 
            loading={loading}
            onBack={() => setSelectedRashi(null)}
            onPeriodChange={setSelectedPeriod}
          />
        )}
      </div>
      {/* </div> */}
    </Layout>
  );
};

export default Horoscope;